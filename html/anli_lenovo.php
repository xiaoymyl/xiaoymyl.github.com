        <p>联想ideapad Z系列针对年轻潮流人群，主打潮流色彩和出众的外观设计。基于对目标人群的洞察——许多年轻人都怀揣着电影梦，当导演，当编剧或当演员，但都苦于无法实现，通往梦想的大门似乎遥不可及。我们认为视频是与年轻族群沟通的良好载体，并且能从视觉上完整凸显产品亮点。因 此，联想、腾讯视频、川制作怀抱着相同信念，帮助年轻人实现梦想，与国内互联网最专业的电影大赛——九分钟电影组委会强强联合，为有热情有梦想的年轻人打开通往电影世界的大门。</p>
